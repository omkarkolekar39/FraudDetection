-- =========================
-- Live Stream Events
-- =========================
-- Current ingestion/live-stream scoring is runtime-first and avoids pushing every streamed row
-- to Supabase. This table remains for compatibility with earlier persistence paths.

create table if not exists public.live_stream_events (
    id bigint generated by default as identity primary key,
    upload_id bigint references public.dataset_uploads(id) on delete set null,
    run_id bigint references public.analytics_runs(id) on delete set null,
    source text not null default 'kafka',
    record_id text not null,
    row_num integer not null,
    category text not null,
    risk_score double precision not null default 0.0,
    ae_error double precision not null default 0.0,
    if_score double precision not null default 0.0,
    payload jsonb not null default '{}'::jsonb,
    created_at timestamptz not null default timezone('utc', now()),
    constraint live_stream_events_row_num_check check (row_num > 0),
    constraint live_stream_events_category_check check (category in ('high', 'medium', 'low', 'unknown'))
);

comment on table public.live_stream_events is 'Compatibility table for Kafka/manual live-row scoring events and CSV analytics snapshots.';

alter table public.live_stream_events alter column payload type jsonb using payload::jsonb;

create index if not exists idx_live_stream_events_created_at on public.live_stream_events (created_at desc);
create index if not exists idx_live_stream_events_upload on public.live_stream_events (upload_id, created_at desc);
create index if not exists idx_live_stream_events_run on public.live_stream_events (run_id, row_num desc, id desc);
create index if not exists idx_live_stream_events_record_id on public.live_stream_events (record_id);
create index if not exists idx_live_stream_events_category on public.live_stream_events (category);
create index if not exists idx_live_stream_events_source on public.live_stream_events (source);

