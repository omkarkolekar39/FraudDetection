-- Run this entire file in the Supabase SQL editor for the database used by SUPABASE_DB_URL.
-- It is intentionally idempotent and can be re-run after app updates.

set search_path = public;

create extension if not exists pgcrypto;

create or replace function public.set_updated_at()
returns trigger
language plpgsql
as $$
begin
    new.updated_at = timezone('utc', now());
    return new;
end;
$$;

-- =========================
-- Users and Access
-- =========================

create table if not exists public.users (
    id bigint generated by default as identity primary key,
    username text not null,
    hashed_password text not null,
    role text not null default 'Viewer',
    is_pending_approval boolean not null default false,
    created_at timestamptz not null default timezone('utc', now()),
    constraint users_username_key unique (username),
    constraint users_role_check check (role in ('Admin', 'Analyst', 'Viewer'))
);

create index if not exists idx_users_username on public.users (username);
create index if not exists idx_users_role on public.users (role);
create index if not exists idx_users_pending on public.users (is_pending_approval);
create index if not exists idx_users_created_at on public.users (created_at desc);

